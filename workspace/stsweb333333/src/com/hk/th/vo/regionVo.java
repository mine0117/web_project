package com.hk.th.vo;

public class regionVo {
	private int map_num;
	private String map_name;
	private String map_categorization;
	
	public int getMap_num() {
		return map_num;
	}
	public void setMap_num(int map_num) {
		this.map_num = map_num;
	}
	public String getMap_name() {
		return map_name;
	}
	public void setMap_name(String map_name) {
		this.map_name = map_name;
	}
	public String getMap_categorization() {
		return map_categorization;
	}
	public void setMap_categorization(String map_categorization) {
		this.map_categorization = map_categorization;
	}
	
	
}
