package com.hk.th.vo;

public class menuVo {
	private int menu_code;
	private String menu_cate;
	private String menu_fname;
	private String price;
	private int res_code;
	public int getMenu_code() {
		return menu_code;
	}
	public void setMenu_code(int menu_code) {
		this.menu_code = menu_code;
	}
	public String getMenu_cate() {
		return menu_cate;
	}
	public void setMenu_cate(String menu_cate) {
		this.menu_cate = menu_cate;
	}
	public String getMenu_fname() {
		return menu_fname;
	}
	public void setMenu_fname(String menu_fname) {
		this.menu_fname = menu_fname;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public int getRes_code() {
		return res_code;
	}
	public void setRes_code(int res_code) {
		this.res_code = res_code;
	}
	
	
	
}
